async function main() {
  const client = await pool.connect();
  console.log('🔗 Fix: parent_agent_id connections');
  console.log('='.repeat(60));
  console.log(`📊 Mapping: ${Object.keys(PARENT_MAP).length} agents to connect`);
  
  let updated = 0;
  let skipped = 0;
  let failed = 0;
  
  for (const [childIdStr, parentId] of Object.entries(PARENT_MAP)) {
    const childId = parseInt(childIdStr);
    try {
      // Only update if currently NULL (idempotent)
      const { rows } = await client.query(
        `UPDATE agents SET parent_agent_id = $1 
         WHERE id = $2 AND parent_agent_id IS NULL 
         RETURNING name`,
        [parentId, childId]
      );
      if (rows[0]) {
        updated++;
        if (updated <= 20 || updated % 50 === 0) {
          console.log(`  ✅ #${childId} → parent #${parentId}: ${rows[0].name?.substring(0, 45)}`);
        }
      } else {
        skipped++;
      }
    } catch (e: any) {
      console.error(`  ❌ #${childId}: ${e.message}`);
      failed++;
    }
  }
  
  console.log(`\n✅ Updated: ${updated} | ⏭ Skipped (already set): ${skipped} | ❌ Failed: ${failed}`);
  
  // Verify result
  const { rows } = await client.query(
    'SELECT COUNT(*) as orphans FROM agents WHERE parent_agent_id IS NULL'
  );
  console.log(`\n📊 Remaining root agents (no parent): ${rows[0].orphans}`);
  
  client.release();
  process.exit(0);
}

main().catch(e => { console.error(e); process.exit(1); });
